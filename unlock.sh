#!/bin/bash
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# GoodWatch Protection Unlock
# Creates a one-time token allowing commits to protected files.
# Token expires after 60 seconds.
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

UNLOCK_FILE="$(git rev-parse --git-dir)/.protection-unlocked"
CURRENT_TIME=$(date +%s)

echo "$CURRENT_TIME" > "$UNLOCK_FILE"

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "  🔓 PROTECTION UNLOCKED"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
echo "  You have 60 seconds to commit protected files."
echo "  Token will be consumed on the next commit."
echo ""
echo "  Run your git commit now!"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
